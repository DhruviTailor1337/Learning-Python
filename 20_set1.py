# sets in python allow us to store multiple items in a single variable

# empty list
myset = {}
print(myset)

# collection of roll numbers(integers)
rollNum = {101,102,103,104,105}
print(rollNum)

# collection of names(Strings)
names = {"Dhruvi", "Trisha","Megh"}
print(names)

# collection of marks(floats)
marks = {91.89, 88.90, 71.00, 32.09}
print(marks)

# collection of all kind of data-type
mixSet = {101, "Dhruvi", 88.90 , True}
print(mixSet)